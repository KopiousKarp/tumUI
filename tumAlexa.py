class LaunchRequestHandler(AbstractRequestHandler):
    #Handler for Skill Launch.
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool

        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        speak_output = "I've sent your email"
        port = 465  # For SSL
        smtp_server = "smtp.gmail.com"
        # don't forget to import credentials
        sender_email = credentials.key['sender_email']  # Enter your address
        receiver_email = credentials.key['receiver_email']  # Enter receiver address
        password = credentials.key['password']  # "given gmail password"
        # Send simple text.
        #message = "This message is sent using smtplib from Python."


        # send multipart message
        message = MIMEMultipart("alternative")
        message["Subject"] = "TUMBOT YOU HAVE ORDERS-Alexa"
        message["From"] = sender_email
        message["To"] = receiver_email

        # add plain text and HTML versions

        text = """\
        You can find perkin at tinyurl.com/y8dk523j
        """
        html = """\
        <html>
          <body>
            <p>Hi,<br>
               <a>Go to evans hall</a>
            </p>
          </body>
        </html>

        """

        # Convert to plain and html MIMEText objects
        plainMIME = MIMEText(text, "plain")
        HTMLMIME = MIMEText(html, "html")

        # Add the HTML and plain-text to the MIME message
        # The email client renders the last part first
        message.attach(plainMIME)
        message.attach(HTMLMIME)

        # and send message as string()
        context = ssl.create_default_context()
        with smtplib.SMTP_SSL("smtp.gmail.com", 465, context=context) as server:
            server.login(sender_email, password)
            server.sendmail(sender_email, receiver_email, message.as_string())

        ################
        # type: (HandlerInput) -> Response
        speak_output = "Welcome, you can say Hello or Help. Which would you like to try?"

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )
